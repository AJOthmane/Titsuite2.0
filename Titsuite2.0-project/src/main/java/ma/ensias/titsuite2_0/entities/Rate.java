package com.sample;


public class Rate {

  private long id;
  private long rate;
  private String review;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public long getRate() {
    return rate;
  }

  public void setRate(long rate) {
    this.rate = rate;
  }


  public String getReview() {
    return review;
  }

  public void setReview(String review) {
    this.review = review;
  }

}
