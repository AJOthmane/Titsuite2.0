package com.sample;


public class Job {

  private long id;
  private long refFreelancer;
  private long refRate;
  private long refOffer;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public long getRefFreelancer() {
    return refFreelancer;
  }

  public void setRefFreelancer(long refFreelancer) {
    this.refFreelancer = refFreelancer;
  }


  public long getRefRate() {
    return refRate;
  }

  public void setRefRate(long refRate) {
    this.refRate = refRate;
  }


  public long getRefOffer() {
    return refOffer;
  }

  public void setRefOffer(long refOffer) {
    this.refOffer = refOffer;
  }

}
